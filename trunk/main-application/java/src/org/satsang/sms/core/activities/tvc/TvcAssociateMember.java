package org.satsang.sms.core.activities.tvc;

/**
 * @author Default
 *
 */
public class TvcAssociateMember {

	private String tvcId;
    private String associateTvcId;
    private String associateRelationship;
    private String firstName;
    private String lastName;
    private String branchName;
    private String gender;
	
    /**
	 * @return the tvcId
	 */
	public String getTvcId() {
		return tvcId;
	}
	/**
	 * @param tvcId the tvcId to set
	 */
	public void setTvcId(String tvcId) {
		this.tvcId = tvcId;
	}
	/**
	 * @return the associateTvcId
	 */
	public String getAssociateTvcId() {
		return associateTvcId;
	}
	/**
	 * @param associateTvcId the associateTvcId to set
	 */
	public void setAssociateTvcId(String associateTvcId) {
		this.associateTvcId = associateTvcId;
	}
	/**
	 * @return the associateRelationship
	 */
	public String getAssociateRelationship() {
		return associateRelationship;
	}
	/**
	 * @param associateRelationship the associateRelationship to set
	 */
	public void setAssociateRelationship(String associateRelationship) {
		this.associateRelationship = associateRelationship;
	}
	/**
	 * @return the firstName
	 */
	public String getFirstName() {
		return firstName;
	}
	/**
	 * @param firstName the firstName to set
	 */
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	/**
	 * @return the lastName
	 */
	public String getLastName() {
		return lastName;
	}
	/**
	 * @param lastName the lastName to set
	 */
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	/**
	 * @return the branchName
	 */
	public String getBranchName() {
		return branchName;
	}
	/**
	 * @param branchName the branchName to set
	 */
	public void setBranchName(String branchName) {
		this.branchName = branchName;
	}
	/**
	 * @return the gender
	 */
	public String getGender() {
		return gender;
	}
	/**
	 * @param gender the gender to set
	 */
	public void setGender(String gender) {
		this.gender = gender;
	}    

}
